import random

    
def random_mail():
    mail = str(random.randrange(1,10000))+'@onet.com'
    return mail

def random_first_name():
    first_name = str(random.randrange(1,10000))+'john'
    return first_name

def random_last_name():
    last_name = str(random.randrange(1,10000))+'Smith'
    return last_name 

def random_password():
    password = str(random.randrange(1,10000))+'Kdkkdk'
    return password

